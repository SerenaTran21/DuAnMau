    private void dangNhap() {
        String maNV = txtMaNV.getText();
        String matKhau = new String(txtMatKhau.getPassword());
        NhanVien nhanVien = dao.selectById(maNV);
        if (nhanVien == null) {
            JOptionPane.showMessageDialog(this, "Sai tên đăng nhập, vui lòng nhập lại.");
        } else if (!matKhau.equals(nhanVien.getMatKhau())) {
            JOptionPane.showMessageDialog(this, "Sai mật khẩu đăng nhập, vui lòng nhập lại.");
        } else {
            Auth.user = nhanVien;
            this.dispose();
        }
    }

